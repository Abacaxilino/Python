# r : Para ler um arquivo, precisamos abri-lo no modo somente leitura usando 'r'.
# w : Para escrever em um arquivo existente. Substituirá tudo o que já existe no arquivo. Se não houver arquivos existentes, criará um novo arquivo com o nome fornecido.
# r+ : Para ler e gravar dados em um arquivo, mas os dados existentes serão apagados e novos dados serão gravados.
# w+ : É usado para gravar e ler dados em um arquivo, mas todos os dados existentes são apagados e novos dados são gravados e armazenados.
# a : O modo de anexação é usado para preservar dados existentes em um arquivo e adicionar mais dados a ele.
